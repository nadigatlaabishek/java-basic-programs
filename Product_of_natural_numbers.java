class  Product_of_natural_numbers
{
	public static void main(String[] args) 
	{
		int product=1;
       System.out.println(" To print the product of natural numbers:.");
		for (int a=1;a<=10 ;a++ )
		{
			product=product*a;
		}
        System.out.println(" The product of natural numbers from "+1+"  to "+10+" is = "+product);
	}
}
